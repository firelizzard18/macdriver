NSNumber * numberWithDouble          (double v)             { return [NSNumber numberWithDouble:v];           }
NSNumber * numberWithFloat           (float v)              { return [NSNumber numberWithFloat:v];            }
NSNumber * numberWithInt             (signed int v)         { return [NSNumber numberWithInt:v];              }
NSNumber * numberWithLong            (signed long v)        { return [NSNumber numberWithLong:v];             }
NSNumber * numberWithLongLong        (signed long long v)   { return [NSNumber numberWithLongLong:v];         }
NSNumber * numberWithShort           (signed short v)       { return [NSNumber numberWithShort:v];            }
NSNumber * numberWithUnsignedChar    (unsigned char v)      { return [NSNumber numberWithUnsignedChar:v];     }
NSNumber * numberWithUnsignedInt     (unsigned int v)       { return [NSNumber numberWithUnsignedInt:v];      }
NSNumber * numberWithUnsignedLong    (unsigned long v)      { return [NSNumber numberWithUnsignedLong:v];     }
